# Main processing loop
# =========================================================

for index, row in enumerate(rows, start=1):

    source = row["File"]

    filename = os.path.basename(source)

    print(f"[{index}/{len(rows)}] {filename}")

    # -----------------------------------------------------

    if os.path.abspath(source).lower() in seen_files:

        report.append([
            filename,
            "SKIPPED",
            "Duplicate filename"
        ])

        skipped += 1

        continue

    seen_files.add(os.path.abspath(source).lower())

    # -----------------------------------------------------

    if not os.path.isfile(source):

        report.append([
            filename,
            "ERROR",
            "Source file missing"
        ])

        errors += 1

        continue

    # -----------------------------------------------------

    duration = get_duration(source)

    if duration is None:

        report.append([
            filename,
            "ERROR",
            "Could not determine duration"
        ])

        errors += 1

        continue

    # -----------------------------------------------------

    cut = calculate_times(row, duration)

    if cut is None:

        report.append([
            filename,
            "SKIPPED",
            "Nothing to cut"
        ])

        skipped += 1

        continue

    start_time, end_time = cut

    # -----------------------------------------------------

    destination = output_dir / filename

    if destination.exists():

        report.append([
            filename,
            "SKIPPED",
            "Already exists"
        ])

        skipped += 1

        continue

    # -----------------------------------------------------

    success = cut_audio(

        str(source),
        str(destination),
        start_time,
        end_time

    )

    if success:

        created += 1

        report.append([
            filename,
            "CREATED",
            f"Intro={start_time:.2f}s  Outro={duration-end_time:.2f}s"
        ])

    else:

        errors += 1

        report.append([
            filename,
            "ERROR",
            "FFmpeg failed"
        ])
